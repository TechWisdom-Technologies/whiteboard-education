Synopsis of Each Module




CRM 501 INTRODUCTION TO ECONOMIC CRIMEThis module provides a critical understanding of the diversity of economic crime. Current trends and issues – from victim responses to the development of regulation will be explored to provide students with a comprehensive understanding of the module.
CRM 502 RESEARCH & ANALYTICAL METHODS IN ECONOMIC CRIME MANAGEMENTThis module is designed as a comprehensive introduction to research methods and statistics. Students are equipped with an understanding of the research process and the practical principles of research design. This module introduces the different types of variables in research and the types of scale on which they can be measured.
CRM 503 FRAUD INVESTIGATIONThe focus of this module is the nature of financial fraud, financial fraud detection, and the investigation of financial fraud. Therefore, this module takes the perspective of organizations, forensic accountants, internal auditors, external auditors, the police, and security specialists. This module takes a holistic view of the mechanics of financial fraud investigations.
CRM 504 PSYCHOLOGY AND CRIMECriminological psychology is applied to provide insights into the understanding, prediction, and treatment of perpetrators of criminal behavior. Accordingly, the focus on the biological and psychological factors in human behavior is the focus of this module. The application of psychological concepts in the legal system is also explored.
CRM 505 ECONOMIC CRIME MANAGEMENT AND TECHNOLOGYCyber-crime is an increasingly pertinent threat in an increasingly technological environment, Combating cyber-crime begins with an understanding of the technologies and issues in combating cyber crime. This extends to understanding the challenges for international rules against cyber-crime; law enforcement; research and technology; and intelligence collection.
CRM 506 INFORMATION, NETWORKS AND INTERNET SECURITYThis module takes a unique perspective of information security by combining both theoretical and conceptual issues with operational and practical activities of information security. The threats and vulnerabilities of existing systems are explored in order to understand the foundations of information security management.
CRM 507 FORENSIC ACCOUNTINGThis module provides a thorough examination of current methods and legal concerns for the detection and prosecution of economic crime. This module is designed as a guide from the detection of economic crime to the indictment and conviction of perpetrators.
CRM 508 ADVANCED FRAUD ANALYSISFamiliarises students with innovative analytic approaches in complex fraud analysis. Discusses basic data mining and predictive analytical skills. Topics include: pre-crime data mining, investigative data warehousing, link analysis, text mining, neural networks, machine learning, intrusion detection, and the Entity Validation System (EVS).
CRM 509 CORPORATE CRIMINAL LIABILITY AND EVIDENTIAL ISSUESDefines the concept of corporate criminal liability, plus a broad review of the theoretical constructs, theories and studies of corporate criminal liability, with analysis of the law and strategic advice for students/practitioners. Emphasises ways to prevent corporate crime via good strategies and compliance policies, in the light of legal developments.
CRM 510 ETHICS IN ECONOMIC CRIME MANAGEMENTFocuses on current moral issues in economic crime management and identifies ethical concepts that provide the foundation for the resolution of ethical issues. This module aims to provide students with an overview of management ethics in building up the integrity value of management.
CRM 511 APPLIED RESEARCH PROJECTThis capstone course is a project that enables candidates to apply all the elements studied in the course in preparing a case study that can be related to policy making, study of a company or a business.
CRM512 CRIMINOLOGYDefines the concepts of criminology and provides a broad review of the theoretical constructs, theories and studies of criminology. It provides both analysis of the criminal behavior and also the many sociological perspectives on commission of crimes.
CRM513 CONTEMPORARY ISSUES IN TERRORISMExamines the phenomena of terrorism from a criminological standpoint. Key definitional debates relating to social, political, media, cultural and ideological ‘construction’ of terror are examined.
